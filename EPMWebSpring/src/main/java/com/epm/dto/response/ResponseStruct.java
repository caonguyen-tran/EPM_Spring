package com.epm.dto.response;

/**
 *
 * @author ACER
 */
public class ResponseStruct<T> {
    private int code = 1000;
    private String status;
    private T result;
    
    public ResponseStruct(String status, T result){
        this.status = status;
        this.result = result;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public T getResult() {
        return result;
    }

    public void setResult(T result) {
        this.result = result;
    }
}
